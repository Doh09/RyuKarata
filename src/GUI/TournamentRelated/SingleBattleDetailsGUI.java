    private void setFieldsToReceivedInfo() {
        //Set the warning lists
        redWarnings = new BattleWarningsTableModel(battle.getRedWarnings());
        blueWarnings = new BattleWarningsTableModel(battle.getBlueWarnings());
        tblRedWarnings.setModel(redWarnings);
        tblBlueWarnings.setModel(blueWarnings);
        //Sets the red buttons
        btnRedGivePoint.setEnabled(battle.getFighter1Red() != null);
        btnRedTakePoint.setEnabled(battle.getFighter1Red() != null);
        btnRedGiveWarning.setEnabled(battle.getFighter1Red() != null);
        btnRedRemoveWarning.setEnabled(battle.getFighter1Red() != null);
        btnCheckRed.setEnabled(battle.getFighter1Red() != null);
        //Sets the blue buttons
        btnBlueGivePoint.setEnabled(battle.getFighter2Blue() != null);
        btnBlueTakePoint.setEnabled(battle.getFighter2Blue() != null);
        btnBlueGiveWarning.setEnabled(battle.getFighter2Blue() != null);
        btnBlueRemoveWarning.setEnabled(battle.getFighter2Blue() != null);
        btnCheckBlue.setEnabled(battle.getFighter2Blue() != null);

        if (battle.getFighter1Red() != null) {
            lblRedPoints.setText("" + battle.getRedPoint());
            lblRedName.setText(battle.getFighter1Red().getName());
        }
        if (battle.getFighter2Blue() != null) {
            lblBluePoints.setText("" + battle.getBluePoint());
            lblBlueName.setText(battle.getFighter2Blue().getName());
        }

        stopWatch = battle.getStopWatch();
        lstStopWatch.setListData(stopWatch.getTimeList().toArray());

        lblTimer.setText(stopWatch.toString());
    }

