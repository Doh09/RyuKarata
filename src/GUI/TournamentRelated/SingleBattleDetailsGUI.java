    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed

        findAndSetWinner();
        //Reset the points.

        if (battle.getFighter1Red() != null) {
            battle.setRedPoint(battle.getRedPoint());
            battle.getFighter1Red().setPoint(Integer.parseInt(lblRedPoints.getText()));
        }
        if (battle.getFighter2Blue() != null) {
            battle.setBluePoint(battle.getBluePoint());
            battle.getFighter2Blue().setPoint(Integer.parseInt(lblBluePoints.getText()));
        }

        dispose();
    }//GEN-LAST:event_btnSaveChangesActionPerformed

    private synchronized void findAndSetWinner() throws NumberFormatException {
        //If both players are in the game, take the one with most points and set them as winner
        if (battle.getFighter1Red() != null && battle.getFighter2Blue() != null) {
            if (battle.getRedPoint() > battle.getBluePoint()) {
                battle.setWinner(battle.getFighter1Red());
            } else if (battle.getRedPoint() < battle.getBluePoint()) {
                battle.setWinner(battle.getFighter2Blue());
            } else if (battle.getRedPoint() == battle.getBluePoint()) {
                battle.setWinner(null);
            }
        } //If one of the players don't exsist, set the other as winner
        else if (battle.getFighter1Red() == null) {
            battle.setWinner(battle.getFighter2Blue());
        } else if (battle.getFighter2Blue() == null) {
            battle.setWinner(battle.getFighter1Red());
        }

    }

